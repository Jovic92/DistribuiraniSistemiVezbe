package domaci;

public class Raspored {
	String[] predmeti;
	String[] vreme;
	int[] sala;
	public String[] getPredmeti() {
		return predmeti;
	}
	public void setPredmeti(String[] predmeti) {
		this.predmeti = predmeti;
	}
	public String[] getVreme() {
		return vreme;
	}
	public void setVreme(String[] vreme) {
		this.vreme = vreme;
	}
	public int[] getSala() {
		return sala;
	}
	public void setSala(int[] sala) {
		this.sala = sala;
	}
	
	
}
